package com.example.myapplication;

public class beach_listview_item {

    private String beach;
    private String wave;
    private String level;


    public void setBeach(String beach2){
        beach = beach2;
    }

    public void setWave(String wave2){
        wave = wave2;
    }

    public void setLevel(String level2){
        level = level2;
    }

    public String getBeach(){
        return this.beach;
    }

    public String getWave(){
        return this.wave;
    }

    public String getLevel(){
        return this.level;
    }
}
